#include "include/headers.h"

using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    bool isStraight(vector<int> &nums) {
        set<int> sets;
        for (int n:nums) {
            if (!n) continue;
            if (sets.find(n) != sets.end()) return false;
            sets.insert(n);
        }
        return *sets.rbegin() - *sets.begin() < 5;
    }
};
