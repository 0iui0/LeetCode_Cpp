/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    vector<vector<int>> levelOrder(TreeNode *root) {
        vector<vector<int>> v;
        if (root == NULL) return v;
        queue<TreeNode *> q;
        q.push(root);
        int nextLv = 0, toBePrint = 1;
        vector<int> tmpV;
        while (!q.empty()) {
            TreeNode *tmp = q.front();
            tmpV.push_back(tmp->val);
            if (tmp->left != NULL) {
                q.push(tmp->left);
                ++nextLv;
            }
            if (tmp->right != NULL) {
                q.push(tmp->right);
                ++nextLv;
            }
            q.pop();
            --toBePrint;
            if (toBePrint == 0) {
                v.push_back(tmpV);
                tmpV.clear();
                toBePrint = nextLv;
                nextLv = 0;
            }
        }
        return v;
    }
};
