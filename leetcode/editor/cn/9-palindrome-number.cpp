class Solution {
public:
    bool isPalindrome(int x) {
        if (x < 0 || (x % 10 == 0 && x != 0)) return false;
        long z  = x;
        long y = 0;
        while(x>0){
            y*=10;
            y+=x%10;
            x/=10;
        }
        return z == y;
    }
};
