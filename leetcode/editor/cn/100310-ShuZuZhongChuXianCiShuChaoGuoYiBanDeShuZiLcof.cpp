//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    int majorityElement(vector<int> &nums) {
        int x = 0, vote = 0;
        for (int n:nums) {
            if (vote == 0) x = n;
            vote += n == x ? 1 : -1;
        }
        return x;
    }
};
